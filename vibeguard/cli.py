    console.print(f"- Git: {git_avail}")
    console.print(f"- Node: {node_avail}")
    console.print(f"- npm: {npm_avail}")
    console.print(f"- Python executable: {display_path(Path(python_exec))}")
    console.print(f"- pytest: {pytest_avail}")
    console.print(f"- ruff: {ruff_avail}")
    console.print(f"- NVIDIA LLM: {nvidia_llm}")
    console.print("")
    console.print("Project:")
    console.print(f"- Git repository: {git_repo}")
    console.print(f"- package.json: {pkg_json}")
    console.print(f"- tsconfig.json: {tsconfig}")
    console.print(f"- .vibeguard folder: {vibeguard_folder}")
    console.print(f"- reports folder: {reports_folder}")
    console.print(f"- Project type: {detection.primary_type}")
    console.print(f"- Package manager: {detection.package_manager or 'None'}")
