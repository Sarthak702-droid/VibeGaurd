@app.command("risk")
def risk_command(project: Path = typer.Option(Path("."), "--project", "-p"), json_output: bool = typer.Option(False, "--json")) -> None:
    """Run deterministic policy and change-risk analysis."""
    root = safe_path(project).resolve()
    diff = analyze_diff(root)
    if diff.status != "ok":
        console.print(f"ERROR: Git diff unavailable ({diff.status}).")
        raise typer.Exit(2)
    findings = policy_findings(diff, load_config(root))
    score = min(100, sum({"INFO": 1, "LOW": 5, "MEDIUM": 15, "HIGH": 30, "CRITICAL": 50}.get(item.severity, 0) for item in findings))
    if json_output:
        console.print(json.dumps({"score": score, "findings": [asdict(item) for item in findings]}, indent=2))
    else:
        console.print(f"Risk score: {score}/100")
        for finding in findings:
            console.print(f"{finding.severity} {finding.file}: {finding.message}")
    if score >= load_config(root).policies.blocking_score:
        raise typer.Exit(1)


