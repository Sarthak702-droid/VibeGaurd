@app.command()
def verify(
    project: Path = typer.Option(Path("."), "--project", "-p", help="Project directory."),
    quick: bool = typer.Option(False, "--quick", help="Run fast checks only."),
    full: bool = typer.Option(False, "--full", help="Run all detected checks."),
    json_output: bool = typer.Option(False, "--json", help="Print machine-readable results."),
    no_banner: bool = typer.Option(False, "--no-banner", help="Disable the startup banner."),
) -> None:
    """Run available verification checks without installing tools."""
    project = safe_path(project)
    if no_banner:
        os.environ["VIBEGUARD_NO_BANNER"] = "1"
    print_banner(console, compact=True, project=project)
    scan_result = scan_project(project)
    if scan_result.detection.package_manager in ("yarn", "pnpm", "bun"):
        console.print(f"WARNING: Detected {scan_result.detection.package_manager} project. Automatic verification currently supports npm in MVP.")
    mode = "quick" if quick else "full"
    report = verify_project(project, scan_result.detection, mode=mode)
    path = write_text(project, "reports/verification_report.md", render_verification_report(report, scan_result.detection.primary_type, scan_result.detection.package_manager or "Unknown"))
    if json_output:
        console.print(json.dumps({"status": report.status, "checks": [asdict(item) for item in report.checks]}, indent=2))
    else:
        console.print("Verification Report")
        for check in report.checks:
            if check.status == "Passed":
                console.print(f"OK {check.name} ({check.duration:.2f}s)")
            elif check.status == "Skipped":
                console.print(f"WARNING {check.name} skipped: {check.details}")
            else:
                console.print(f"FAILED {check.name}: {check.details[:160]}")
        console.print(f"Report written: {display_path(path)}")
    if report.status == "Failed":
        raise typer.Exit(1)


