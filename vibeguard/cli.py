def _current_report(root: Path):
    scan_result = scan_project(root)
    verification = verify_project(root, scan_result.detection)
    diff = analyze_diff(root)
    findings = scan_secrets(root) + scan_dependencies(root, diff if diff.status == "ok" else None)
    changed_files: list[str] = []
    if diff.status == "ok":
        findings += policy_findings(diff, load_config(root))
        changed_files = [item.path for item in diff.changed_files]
    latest = ensure_vibeguard_dir(root) / "sessions" / "latest.json"
    session = json.loads(latest.read_text(encoding="utf-8")) if latest.exists() else {}
    checks = [asdict(item) for item in verification.checks]
    return build_report(root, findings=findings, checks=checks, files_changed=changed_files, agent=session.get("agent"), task=session.get("task"))


def _postflight(root: Path, agent: str, task: str | None, session_files: list[str]) -> bool:
    report = _current_report(root)
    if session_files and not report.files_changed:
        report = build_report(root, findings=report.findings, checks=report.checks, files_changed=session_files, agent=agent, task=task)
    for format_name in ("markdown", "json"):
        write_report(root, report, format_name)
    console.print(f"Postflight decision: {report.decision} (risk {report.score}/100)")
    return report.decision == "BLOCKED"


